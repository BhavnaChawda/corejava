package OopsPractical;

public class PersonDetail {
String name=null;
int dob=0;
int age=0;
public void setName(String name) {
	this.name=name;
}
public String getName() {
	return name;
}
public void setDob(int dob) {
	this.dob=dob;
}
public int getDob() {
	return dob;
	
}
public void setAge(int age) {
	this.age=age;
}
public int getAge() {
	return age;
}
}
